package ru.nsu.fit.bozhko.tools;

public class Span {
    private int y;
    private int x1;
    private int x2;

    public void setX1(int x1) {
        this.x1 = x1;
    }

    public void setX2(int x2) {
        this.x2 = x2;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getY() {
        return y;
    }

    public int getX1() {
        return x1;
    }

    public int getX2() {
        return x2;
    }
}
